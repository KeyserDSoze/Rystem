/**
 * Platform types for social authentication
 */
export enum PlatformType {
    /**
     * Web browser platform (default)
     */
    Web = 'web',
    
    /**
     * iOS mobile platform
     */
    iOS = 'ios',
    
    /**
     * Android mobile platform
     */
    Android = 'android',
    
    /**
     * Auto-detect platform
     */
    Auto = 'auto'
}
